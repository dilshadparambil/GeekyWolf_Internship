using System;
using System.Collections.Generic;
using System.Diagnostics.Metrics;
using System.Linq;
using System.Text;
using System.Threading.Tasks;


namespace day2
{
    public class Q01
    {
        /// <summary>
        /// print first 20 numbers using for loop
        /// </summary>
        public void Answer()
        {
            for (int counter = 1; counter <= 20; counter++) 
            {
                Console.WriteLine($"number = {counter}");
            }
            Console.WriteLine();
        }
         
    }
    
}
